func TestHasActiveMappings(t *testing.T) {
	tests := []struct {
		name string
		cfg  *config.Config
		want bool
	}{
		{
			name: "nil config",
			cfg:  nil,
			want: false,
		},
		{
			name: "disabled auth",
			cfg: &config.Config{PackageAuth: config.PackageAuthConfig{
				Enabled:    false,
				Registries: []config.PackageAuthRegistry{{Registry: "https://registry.npmjs.org/", Enabled: true}},
			}},
			want: false,
		},
		{
			name: "enabled with no active registries",
			cfg: &config.Config{PackageAuth: config.PackageAuthConfig{
				Enabled:    true,
				Registries: []config.PackageAuthRegistry{{Registry: "https://registry.npmjs.org/", Enabled: false}},
			}},
			want: false,
		},
		{
			name: "enabled with active registry",
			cfg: &config.Config{PackageAuth: config.PackageAuthConfig{
				Enabled:    true,
				Registries: []config.PackageAuthRegistry{{Registry: "https://registry.npmjs.org/", Enabled: true}},
			}},
			want: true,
		},
	}

	for _, test := range tests {
		if got := HasActiveMappings(test.cfg); got != test.want {
			t.Fatalf("%s: HasActiveMappings() = %t, want %t", test.name, got, test.want)
		}
	}
}

func TestHasStoredToken(t *testing.T) {
	originalBackend := backend
	backend = fakeKeychain{values: map[string]string{
		"https://registry.npmjs.org/": "token-a",
	}}
	defer func() {
		backend = originalBackend
	}()

	present, err := HasStoredToken("registry.npmjs.org")
	if err != nil {
		t.Fatalf("HasStoredToken returned error: %v", err)
	}
	if !present {
		t.Fatal("expected token to be present")
	}

	present, err = HasStoredToken("npm.pkg.github.com")
	if err != nil {
		t.Fatalf("HasStoredToken returned error for missing token: %v", err)
	}
	if present {
		t.Fatal("expected missing token to report false")
	}
}

