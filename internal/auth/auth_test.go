func TestNormalizeRegistry(t *testing.T) {
	tests := map[string]string{
		"registry.npmjs.org":              "https://registry.npmjs.org/",
		"https://NPM.pkg.github.com":      "https://npm.pkg.github.com/",
		"https://example.com/private/npm": "https://example.com/private/npm/",
	}

	for input, want := range tests {
		got, err := NormalizeRegistry(input)
		if err != nil {
			t.Fatalf("NormalizeRegistry(%q) returned error: %v", input, err)
		}
		if got != want {
			t.Fatalf("NormalizeRegistry(%q) = %q, want %q", input, got, want)
		}
	}
}

func TestBuildUserConfig(t *testing.T) {
	originalBackend := backend
	backend = fakeKeychain{values: map[string]string{
		"https://registry.npmjs.org/":      "token-a",
		"https://npm.pkg.github.com/":      "token-b",
		"https://example.com/private/npm/": "token-c",
	}}
	defer func() {
		backend = originalBackend
	}()

	registries := []config.PackageAuthRegistry{
		{Registry: "https://registry.npmjs.org/", Enabled: true},
		{Registry: "https://npm.pkg.github.com/", Scope: "my-org", Enabled: true},
		{Registry: "https://example.com/private/npm", Enabled: true},
	}

	content, warnings, err := buildUserConfig(registries, []string{"HOME=/tmp"})
	if err != nil {
		t.Fatalf("buildUserConfig returned error: %v", err)
	}
	if len(warnings) != 0 {
		t.Fatalf("buildUserConfig returned warnings: %v", warnings)
	}

	for _, want := range []string{
		"; generated by nodeman",
		"//registry.npmjs.org/:_authToken=token-a",
		"@my-org:registry=https://npm.pkg.github.com/",
		"//npm.pkg.github.com/:_authToken=token-b",
		"//example.com/private/npm/:_authToken=token-c",
	} {
		if !strings.Contains(content, want) {
			t.Fatalf("expected generated config to contain %q, got:\n%s", want, content)
		}
	}
}

func TestSetEnv(t *testing.T) {
	base := []string{"PATH=/bin", "npm_config_userconfig=/old/path", "HOME=/tmp"}
	updated := setEnv(base, "NPM_CONFIG_USERCONFIG", "/new/path")

	want := []string{"PATH=/bin", "NPM_CONFIG_USERCONFIG=/new/path", "HOME=/tmp"}
	if !reflect.DeepEqual(updated, want) {
		t.Fatalf("setEnv() = %#v, want %#v", updated, want)
	}
}

